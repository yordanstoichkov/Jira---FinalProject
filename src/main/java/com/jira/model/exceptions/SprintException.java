package com.jira.model.exceptions;

public class SprintException extends Exception {

	private static final long serialVersionUID = 7803556841407635783L;

	public SprintException() {
		super();
	}

	public SprintException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SprintException(String arg0) {
		super(arg0);
	}

	public SprintException(Throwable arg0) {
		super(arg0);
	}

}
